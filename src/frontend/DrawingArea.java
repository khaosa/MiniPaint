    @Override
    public void mouseDragged(MouseEvent e) {

        Point releasedPoint = new Point(e.getX(), e.getY());
        if (shapeBeingMoved!=null && resizing  ) //resizing shape
        {
            shapeBeingMoved.resize(releasedPoint);
            this.refresh(this.getGraphics());
            
        }

        else if (shapeBeingMoved != null) { // there is a shape being moved but not resized
            shapeBeingMoved.moveTo(releasedPoint);
            this.refresh(this.getGraphics());
        }

    }

    @Override
    public void mouseMoved(MouseEvent e) {

    }

    public void clearShapes() {
        MainFrame parentFrame = (MainFrame) this.getParentNode();
        for (int i = drawnShapes.size() - 1; i >= 0; i--) {
            drawnShapes.remove(i);
            parentFrame.getChooseShapeComboBox().removeItemAt(i + 1);

        }
        this.C = 1;
        this.R = 1;
        this.L = 1;
        this.T = 1;

    }
