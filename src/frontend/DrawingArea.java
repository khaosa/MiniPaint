        super.paintComponent(g);
        for (int i = 0; i < drawnShapes.size(); i++) {
            drawnShapes.get(i).draw(g);
            if (drawnShapes.get(i) == shapeBeingMoved) {
                shapeBeingMoved.drawResizingPoints(g);
            }
        }

